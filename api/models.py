from pydantic import BaseModel

############################################################################
# Question model
############################################################################
class Question(BaseModel):
    question: str

############################################################################
# Response model
############################################################################    
class Response(BaseModel):
    response: str

############################################################################
# Delete Folder model
############################################################################  
class DeleteFolderRequest(BaseModel):
    bucket: str
    folder_name: str

############################################################################
# Create Folder model
############################################################################  
class CreateFolderRequest(BaseModel):
    folder_name: str 
    bucket: str
     